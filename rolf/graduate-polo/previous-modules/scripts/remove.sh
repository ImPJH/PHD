# 여러 디렉토리 목록을 배열로 선언합니다.
directories=("./seed_comparison/figures/full/fixed" "./seed_comparison/figures/full/unfixed")

# 각 디렉토리에 대해 반복합니다.
for DIRECTORY in "${directories[@]}"; do
    # 디렉토리 내의 모든 파일에 대해 반복합니다.
    for filename in "$DIRECTORY"/*; do
        # 파일 이름에 num_list의 숫자 중 하나라도 포함되어 있지 않으면 해당 파일을 삭제합니다.
        remove_file=true
        for num in "${num_list[@]}"; do
            if [[ "$filename" =~ "$num" ]]; then
                remove_file=false
                break
            fi
        done
        if $remove_file; then
            rm "$filename"
            # echo "$filename"
        fi
    done
done

# 이 스크립트를 사용하기 전에 다음 사항을 주의하세요:

# DIRECTORY 변수를 원하는 디렉토리 경로로 변경하세요.
